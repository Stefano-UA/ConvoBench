#   в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ
#   в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•җв•қ в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв–Ҳв•—
#   в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘
#   в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ
#   в•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•җв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ

# <==========================================================>
#                   Benchmark CSV Ranker
# <==========================================================>
#  Check if arguments were passed
# <==========================================================>
if [ "$#" -eq 0 ]; then
    echo "Usage: $0 <benchmark_1.csv> [benchmark_2.csv ...]"
    exit 1
fi
# <==========================================================>
#  Print header
# <==========================================================>
echo "<===============================================================================================>"
echo "                                   CSV RANKING REPORT                                    "
echo "<===============================================================================================>"
# <==========================================================>
#  Temporary files to hold the minimums
# <==========================================================>
TMP_TOTAL="$(mktemp)"
TMP_CONV="$(mktemp)"
# <==========================================================>
#  Iterate over all provided files
# <==========================================================>
for file in "$@"; do
    if [ ! -f "$file" ]; then
        echo " [WARNING] File not found: ${file}"
        continue
    fi
    # Extract minimum Total_s (Column 11 in our CSV structure)
    min_total=$(awk -F';' 'NR>1 && $11 != "" {print $11}' "$file" | sort -n | head -n 1)
    # Extract minimum Conv_s (Column 16 in our CSV structure)
    min_conv=$(awk -F';' 'NR>1 && $16 != "" {print $16}' "$file" | sort -n | head -n 1)
    if [ -n "$min_total" ]; then
        echo "${min_total} ${file}" >> "$TMP_TOTAL"
    fi
    if [ -n "$min_conv" ]; then
        echo "${min_conv} ${file}" >> "$TMP_CONV"
    fi
done
# <==========================================================>
#  Global Ranking: Total Time
# <==========================================================>
echo " Ranking by Absolute Best Total Time (Total_s):"
echo "<----------------------------------------------------------------------------------------------->"
printf " %-10s | %-15s | %-50s
" "Rank" "Best Time (s)" "File"
echo "<----------------------------------------------------------------------------------------------->"
rank=1
while read -r time file; do
    printf " %-10s | %-15s | %-50s\n" "#${rank}" "${time}" "$(basename "$file")"
    rank=$((rank + 1))
done < <(sort -n "$TMP_TOTAL")
echo ""
echo "<===============================================================================================>"
# <==========================================================>
#  Global Ranking: Convolution Time
# <==========================================================>
echo " Ranking by Absolute Best Convolution Time (Conv_s):"
echo "<----------------------------------------------------------------------------------------------->"
printf " %-10s | %-15s | %-50s
" "Rank" "Best Time (s)" "File"
echo "<----------------------------------------------------------------------------------------------->"
rank=1
while read -r time file; do
    printf " %-10s | %-15s | %-50s\n" "#${rank}" "${time}" "$(basename "$file")"
    rank=$((rank + 1))
done < <(sort -n "$TMP_CONV")
echo "<===============================================================================================>"
# <==========================================================>
#  Cleanup
# <==========================================================>
rm -f "$TMP_TOTAL" "$TMP_CONV"
